public class FunctionsSum {
    public static int twoNumbersSum(int a, int b)
    {
        int sum;
        sum = a + b;
        return sum;
    }
    public static void main(String[] args)
    {
        int x = 12;
        int y = 3;
        int result;
        result= twoNumbersSum(x, y);
        System.out.println(result);
    }
}
